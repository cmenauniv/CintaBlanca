//Funcion para saber el área de un cuadrado
const cuadrado = (x) => {
    return x*x
}

console.log(cuadrado(10))

//Funcion para saber el área de un rectánculo
const rectangulo = (x,y) => {
    return x*y
}

console.log(rectangulo(10,5))

//Funcion para saber el área de un triángulo
const triangulo = (x,y) => {
    return (x*y)/2
}

console.log(triangulo(10,5))